# ============= RESNET MODEL =============
class MultiHeadResNet50(nn.Module):
    def __init__(self, num_gender, num_age, num_ethnicity):
        super().__init__()
        self.resnet = models.resnet50(weights=None)
        num_ftrs = self.resnet.fc.in_features
        self.resnet.fc = nn.Identity()

        self.shared_features = nn.Sequential(
            nn.Linear(num_ftrs, 1024), nn.ReLU(), nn.BatchNorm1d(1024), nn.Dropout(0.3),
            nn.Linear(1024, 512), nn.ReLU(), nn.BatchNorm1d(512), nn.Dropout(0.3)
        )
        self.gender_head = nn.Sequential(
            nn.Linear(512, 256), nn.ReLU(), nn.Dropout(0.2), nn.Linear(256, num_gender)
        )
        self.age_head = nn.Sequential(
            nn.Linear(512, 256), nn.ReLU(), nn.Dropout(0.2), nn.Linear(256, num_age)
        )
        self.ethnicity_head = nn.Sequential(
            nn.Linear(512, 256), nn.ReLU(), nn.Dropout(0.2), nn.Linear(256, num_ethnicity)
        )

    def forward(self, x):
        x = self.resnet(x)
        x = self.shared_features(x)
        return self.gender_head(x), self.age_head(x), self.ethnicity_head(x)


# ============= ENHANCED CLASSIFIER WITH ETHNICITY FALLBACK =============
class AttributeClassifier:
    def __init__(self, model_path: str, device: torch.device, logger: logging.Logger, config: Config):
        self.device = device
        self.logger = logger
        self.config = config

        self.logger.info("Loading ResNet Classifier...")
        checkpoint = torch.load(model_path, map_location=device, weights_only=False)
        checkpoint_config = checkpoint['config']

        self.gender_classes = checkpoint_config['gender_classes']
        self.age_classes = checkpoint_config['age_classes']
        self.ethnicity_classes = checkpoint_config['ethnicity_classes']

        self.model = MultiHeadResNet50(
            checkpoint_config['num_gender_classes'],
            checkpoint_config['num_age_classes'],
            checkpoint_config['num_ethnicity_classes']
        ).to(device)

        self.model.load_state_dict(checkpoint['model_state_dict'])
        self.model.eval()

        self.transform = transforms.Compose([
            transforms.Resize((224, 224)),
            transforms.ToTensor(),
            transforms.Normalize([0.485, 0.456, 0.406], [0.229, 0.224, 0.225])
        ])

        self.classification_count = 0
        self.failed_classifications = 0
        self.ethnicity_unknown_count = 0

    @torch.no_grad()
    def classify(self, image: np.ndarray) -> Optional[Dict]:
        """Classify with ethnicity fallback to 'unknown' if low confidence"""
        try:
            if image.size == 0 or image.shape[0] < 40 or image.shape[1] < 40:
                self.failed_classifications += 1
                return None

            # Check image quality
            gray = cv2.cvtColor(image, cv2.COLOR_BGR2GRAY)
            blur = cv2.Laplacian(gray, cv2.CV_64F).var()
            if blur < 25:  # Very low threshold for crowds
                self.failed_classifications += 1
                return None

            img_rgb = cv2.cvtColor(image, cv2.COLOR_BGR2RGB)
            img_pil = Image.fromarray(img_rgb)
            img_tensor = self.transform(img_pil).unsqueeze(0).to(self.device)

            gender_out, age_out, ethnicity_out = self.model(img_tensor)

            gender_probs = torch.softmax(gender_out, 1)
            age_probs = torch.softmax(age_out, 1)
            ethnicity_probs = torch.softmax(ethnicity_out, 1)

            gender_conf, gender_pred = torch.max(gender_probs, 1)
            age_conf, age_pred = torch.max(age_probs, 1)
            ethnicity_conf, ethnicity_pred = torch.max(ethnicity_probs, 1)

            # ===== ETHNICITY FALLBACK LOGIC =====
            ethnicity_class = self.ethnicity_classes[ethnicity_pred.item()]
            ethnicity_confidence = ethnicity_conf.item()

            # If ethnicity confidence is too low, assign "unknown"
            if self.config.ETHNICITY_UNKNOWN_FALLBACK and ethnicity_confidence < self.config.ETHNICITY_CONFIDENCE_THRESHOLD:
                ethnicity_class = "unknown"
                ethnicity_confidence = 1.0  # Set to 1.0 for "unknown"
                self.ethnicity_unknown_count += 1

            self.classification_count += 1

            return {
                'gender': {'class': self.gender_classes[gender_pred.item()], 'confidence': gender_conf.item()},
                'age': {'class': self.age_classes[age_pred.item()], 'confidence': age_conf.item()},
                'ethnicity': {'class': ethnicity_class, 'confidence': ethnicity_confidence}
            }
        except Exception as e:
            self.logger.error(f"Classification error: {e}")
            self.failed_classifications += 1
            return None

    def get_stats(self) -> Dict:
        """Return classification statistics"""
        total = self.classification_count + self.failed_classifications
        return {
            'total_classifications': self.classification_count,
            'failed_classifications': self.failed_classifications,
            'ethnicity_unknown_assigned': self.ethnicity_unknown_count,
            'success_rate': (self.classification_count / total * 100) if total > 0 else 0
        }


# ============= ENHANCED TRACK MANAGER WITH MULTI-FRAME VOTING =============
class TrackManager:
    """Manages track history with persistent classifications and multi-frame voting"""
    def __init__(self, config: Config, logger: logging.Logger):
        self.config = config
        self.logger = logger

        # Track data structure
        self.tracks = {}
        self.classification_persistence = {}

        # Multi-frame classification buffers
        self.classification_buffer = defaultdict(lambda: {
            'gender': deque(maxlen=config.CLASSIFICATION_BUFFER_SIZE),
            'age': deque(maxlen=config.CLASSIFICATION_BUFFER_SIZE),
            'ethnicity': deque(maxlen=config.CLASSIFICATION_BUFFER_SIZE)
        })

        # NEW: Label position smoothing cache
        self.label_positions = {}

        # Statistics
        self.stats = {
            'tracks_created': 0,
            'tracks_classified': 0,
            'classifications_recovered': 0,
            'tracks_lost': 0,
            'reid_assists': 0,
            'id_switches_prevented': 0
        }

    def update_track(self, track_id: int, frame_num: int):
        """Update track information"""
        if track_id not in self.tracks:
            self.tracks[track_id] = {
                'first_seen': frame_num,
                'last_seen': frame_num,
                'detections': 1,
                'classification': None,
                'classification_locked': False,
                'classification_frame': None,
                'reid_maintained': False,
                'gap_count': 0
            }
            self.stats['tracks_created'] += 1
        else:
            prev_last_seen = self.tracks[track_id]['last_seen']
            gap = frame_num - prev_last_seen

            self.tracks[track_id]['last_seen'] = frame_num
            self.tracks[track_id]['detections'] += 1

            # Detect if ReID maintained the ID (gap > 20 frames)
            if gap > 20:
                self.tracks[track_id]['gap_count'] += 1

                if not self.tracks[track_id]['reid_maintained'] and gap > 30:
                    self.tracks[track_id]['reid_maintained'] = True
                    self.stats['reid_assists'] += 1
                    self.stats['id_switches_prevented'] += 1

                    if self.config.ENABLE_TRACK_DEBUG:
                        self.logger.info(f"🔍 ReID maintained Track {track_id} after {gap} frame gap")

    def smooth_label_position(self, track_id: int, bbox: Tuple[int, int, int, int]) -> Tuple[int, int]:
        """Smooth label position to avoid jitter"""
        x1, y1, x2, y2 = bbox
        current_pos = (x1, y1)

        if not self.config.ENABLE_LABEL_SMOOTHING:
            return current_pos

        if track_id not in self.label_positions:
            self.label_positions[track_id] = current_pos
            return current_pos

        # Exponential smoothing
        prev_x, prev_y = self.label_positions[track_id]
        alpha = self.config.LABEL_SMOOTHING_ALPHA

        smooth_x = int(alpha * x1 + (1 - alpha) * prev_x)
        smooth_y = int(alpha * y1 + (1 - alpha) * prev_y)

        smoothed = (smooth_x, smooth_y)
        self.label_positions[track_id] = smoothed

        return smoothed

    def add_classification_vote(self, track_id: int, classification: Dict):
        """Add a classification vote to the buffer"""
        buffer = self.classification_buffer[track_id]
        buffer['gender'].append(classification['gender'])
        buffer['age'].append(classification['age'])
        buffer['ethnicity'].append(classification['ethnicity'])

    def get_voted_classification(self, track_id: int) -> Optional[Dict]:
        """Get classification through confidence-weighted voting"""
        if track_id not in self.classification_buffer:
            return None

        buffer = self.classification_buffer[track_id]

        # Need minimum votes
        if len(buffer['gender']) < self.config.MIN_VOTES_FOR_LOCK:
            return None

        def weighted_vote(predictions):
            """Vote by summing confidences for each class"""
            if not predictions:
                return None

            votes = defaultdict(float)
            for pred in predictions:
                votes[pred['class']] += pred['confidence']

            if not votes:
                return None

            winner = max(votes.items(), key=lambda x: x[1])
            avg_confidence = winner[1] / len(predictions)

            return {'class': winner[0], 'confidence': avg_confidence}

        voted = {
            'gender': weighted_vote(buffer['gender']),
            'age': weighted_vote(buffer['age']),
            'ethnicity': weighted_vote(buffer['ethnicity'])
        }

        # Check if all categories have votes
        if any(v is None for v in voted.values()):
            return None

        return voted

    def set_classification(self, track_id: int, classification: Dict, frame_num: int):
        """Lock classification for a track (PERMANENT)"""
        if track_id in self.tracks:
            self.tracks[track_id]['classification'] = classification
            self.tracks[track_id]['classification_locked'] = True
            self.tracks[track_id]['classification_frame'] = frame_num
            self.stats['tracks_classified'] += 1

            # Store in persistence cache
            self.classification_persistence[track_id] = {
                'classification': classification,
                'frame': frame_num
            }

            if self.config.ENABLE_TRACK_DEBUG:
                gender = classification['gender']['class']
                ethnicity = classification['ethnicity']['class']
                self.logger.info(f"✓ Track {track_id} LOCKED: {gender}, {ethnicity}")

    def get_classification(self, track_id: int, current_frame: int) -> Optional[Dict]:
        """Get classification for a track with persistence"""
        # First check if track has locked classification
        if track_id in self.tracks and self.tracks[track_id].get('classification_locked'):
            return self.tracks[track_id]['classification']

        # Check persistence cache
        if track_id in self.classification_persistence:
            cached = self.classification_persistence[track_id]
            frames_since = current_frame - cached['frame']

            if frames_since < self.config.KEEP_CLASSIFICATION_FRAMES:
                if self.config.ENABLE_TRACK_DEBUG and frames_since > 60:
                    self.logger.info(f"✓ Track {track_id} classification recovered ({frames_since} frames gap)")
                    self.stats['classifications_recovered'] += 1
                return cached['classification']

        return None

    def should_classify(self, track_id: int, frame_num: int) -> bool:
        """Determine if track should be classified now"""
        if track_id not in self.tracks:
            return False

        track = self.tracks[track_id]

        # Don't re-classify if locked
        if track.get('classification_locked'):
            return False

        # Need minimum detections
        if track['detections'] < self.config.MIN_DETECTIONS_FOR_CLASSIFICATION:
            return False

        # If never classified, yes
        if track['classification'] is None:
            return True

        # If lost for too long, re-classify
        if track['classification_frame'] is not None:
            frames_since = frame_num - track['classification_frame']
            if frames_since > self.config.RECLASSIFY_IF_LOST_FRAMES:
                return True

        return False

    def cleanup(self, current_frame: int):
        """Remove very old tracks"""
        dead_ids = []
        for track_id, track in self.tracks.items():
            age = current_frame - track['last_seen']
            if age > 600:  # 20 seconds at 30fps
                dead_ids.append(track_id)
                if track.get('classification_locked') and self.config.ENABLE_TRACK_DEBUG:
                    self.logger.warning(f"✗ Track {track_id} removed (age: {age} frames)")
                    self.stats['tracks_lost'] += 1

        for track_id in dead_ids:
            del self.tracks[track_id]
            if track_id in self.classification_buffer:
                del self.classification_buffer[track_id]
            if track_id in self.label_positions:
                del self.label_positions[track_id]

    def get_stats(self) -> Dict:
        return {
            'active_tracks': len(self.tracks),
            **self.stats
        }


# ============= HELPERS =============
def validate_zones(zones: Dict[str, List]) -> Tuple[bool, List[str]]:
    errors = []
    for zone_name in ['entrance', 'store', 'exit']:
        if zone_name not in zones:
            errors.append(f"Missing zone: {zone_name}")
        elif len(zones[zone_name]) < 3:
            errors.append(f"Zone '{zone_name}' needs 3+ points")
    return len(errors) == 0, errors


def point_in_zone(cx: int, cy: int, polygon: List) -> bool:
    try:
        pts = np.array(polygon, dtype=np.int32)
        return cv2.pointPolygonTest(pts, (int(cx), int(cy)), False) >= 0
    except:
        return False


def draw_zone(frame: np.ndarray, polygon: List, color: Tuple, label: str):
    try:
        pts = np.array(polygon, dtype=np.int32).reshape((-1, 1, 2))
        cv2.polylines(frame, [pts], True, color, 2)
        cv2.putText(frame, label, tuple(polygon[0]), cv2.FONT_HERSHEY_SIMPLEX, 0.7, color, 2)
    except:
        pass


def smooth_bbox(prev_bbox: Optional[Tuple], new_bbox: Tuple, alpha: float = 0.75) -> Tuple:
    if prev_bbox is None:
        return new_bbox
    try:
        prev = np.array(prev_bbox, dtype=float)
        new = np.array(new_bbox, dtype=float)
        smoothed = alpha * new + (1 - alpha) * prev
        return tuple(smoothed.astype(int))
    except:
        return new_bbox


# ============= GROUND TRUTH ANALYZER =============
class GroundTruthAnalyzer:
    """Collects detection data for comprehensive analytics"""
    def __init__(self):
        self.detections = []
        self.frame_stats = defaultdict(lambda: {'total_detections': 0, 'high_conf_detections': 0})

    def add_detection(self, frame_num: int, track_id: int, bbox: Tuple, confidence: float,
                     in_zone: bool, classification: Optional[Dict] = None):
        """Record a detection for later analysis"""
        x1, y1, x2, y2 = bbox
        bbox_area = (x2 - x1) * (y2 - y1)

        self.detections.append({
            'frame': frame_num,
            'track_id': track_id,
            'x1': x1,
            'y1': y1,
            'x2': x2,
            'y2': y2,
            'bbox_area': bbox_area,
            'confidence': confidence,
            'in_store_zone': in_zone,
            'gender': classification['gender']['class'] if classification else 'unknown',
            'age': classification['age']['class'] if classification else 'unknown',
            'ethnicity': classification['ethnicity']['class'] if classification else 'unknown',
            'gender_conf': classification['gender']['confidence'] if classification else 0.0,
            'age_conf': classification['age']['confidence'] if classification else 0.0,
            'ethnicity_conf': classification['ethnicity']['confidence'] if classification else 0.0
        })

        # Track per-frame statistics
        self.frame_stats[frame_num]['total_detections'] += 1
        if confidence >= 0.5:
            self.frame_stats[frame_num]['high_conf_detections'] += 1

