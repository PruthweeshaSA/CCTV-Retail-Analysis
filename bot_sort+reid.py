    def _draw_label(self, frame, bbox, attributes, track_id, is_locked=False):
        """Draw classification label with SMOOTH positioning"""
        x1, y1, x2, y2 = bbox

        # Get smoothed label position
        label_x, label_y = self.track_manager.smooth_label_position(track_id, bbox)

        gender = attributes['gender']['class'].lower()
        color = self.config.COLORS.get(gender, self.config.COLORS['unknown'])

        # Thicker border if locked
        thickness = 4 if is_locked else 3
        cv2.rectangle(frame, (x1, y1), (x2, y2), color, thickness)

        lines = [
            f"ID: {track_id}{'*' if is_locked else ''}",
            attributes['gender']['class'].capitalize(),
            attributes['age']['class'].replace('_', ' ').title(),
            attributes['ethnicity']['class'].replace('_', ' ').title()
        ]

        font = cv2.FONT_HERSHEY_SIMPLEX
        y_offset = label_y - 10

        for line in lines:
            # Shadow
            cv2.putText(frame, line, (label_x+1, y_offset+1), font, 0.5, (0,0,0), 2)
            # Text
            cv2.putText(frame, line, (label_x, y_offset), font, 0.5, color, 2)
            y_offset -= 18

        return frame

    def process_video(self, video_path: str, zones: Dict) -> Dict:
        """Process video with optimized tracking and ground truth collection"""
        start_time = datetime.now()

        is_valid, errors = validate_zones(zones)
        if not is_valid:
            raise ValueError(f"Invalid zones: {errors}")

        cap = cv2.VideoCapture(video_path)
        if not cap.isOpened():
            raise ValueError(f"Cannot open: {video_path}")

        fps = int(cap.get(cv2.CAP_PROP_FPS))
        width = int(cap.get(cv2.CAP_PROP_FRAME_WIDTH))
        height = int(cap.get(cv2.CAP_PROP_FRAME_HEIGHT))
        total_frames = int(cap.get(cv2.CAP_PROP_FRAME_COUNT))

        self.logger.info(f"Video: {video_path}")
        self.logger.info(f"{width}x{height} @ {fps}fps, {total_frames} frames")

        # Output
        timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
        os.makedirs("output", exist_ok=True)
        video_name = os.path.splitext(os.path.basename(video_path))[0]
        out_video = f"output/{video_name}_{timestamp}_optimized.mp4"
        out_json = f"output/{video_name}_{timestamp}_optimized.json"
        out_csv = f"output/{video_name}_{timestamp}_detections.csv"

        writer = cv2.VideoWriter(out_video, cv2.VideoWriter_fourcc(*'mp4v'), fps, (width, height))

        entrance = zones['entrance']
        store = zones['store']
        exit_zone = zones['exit']

        # State
        person_states = {}
        footfall_ids = set()
        passby_ids = set()
        unique_visitors = set()
        bbox_cache = {}

        male_count = female_count = 0
        stats = {'gender': defaultdict(int), 'age': defaultdict(int), 'ethnicity': defaultdict(int)}

        self.logger.info("🚀 Processing with optimized crowd detection...")
        pbar = tqdm(total=total_frames, unit="f")

        frame_num = 0
        tracking_errors = 0

        while True:
            try:
                ret, frame = cap.read()
                if not ret:
                    break

                if frame_num % self.config.CLEANUP_EVERY == 0:
                    gc.collect()
                    if torch.cuda.is_available():
                        try:
                            torch.cuda.empty_cache()
                        except:
                            pass
                    self.track_manager.cleanup(frame_num)

                # ===== OPTIMIZED TRACKING =====
                try:
                    results = self.yolo.track(
                        frame,
                        conf=self.config.CONFIDENCE,
                        iou=self.config.IOU_THRESHOLD,
                        imgsz=self.config.IMG_SIZE,
                        tracker=f"configs/{self.config.TRACKER_CONFIG}",
                        persist=True,
                        verbose=False,
                        classes=[0],
                        max_det=self.config.MAX_DET
                    )
                except Exception as track_error:
                    tracking_errors += 1
                    if tracking_errors == 1:
                        self.logger.error(f"Tracking error: {track_error}")
                    frame_num += 1
                    pbar.update(1)
                    continue

                # Draw zones
                draw_zone(frame, entrance, self.config.COLORS['entrance'], "ENTRANCE")
                draw_zone(frame, store, self.config.COLORS['store'], "STORE")
                draw_zone(frame, exit_zone, self.config.COLORS['exit'], "EXIT")

                # Process tracks
                if results[0].boxes is not None and results[0].boxes.id is not None:
                    boxes = results[0].boxes.xyxy.cpu().numpy()
                    track_ids = results[0].boxes.id.cpu().numpy().astype(int)
                    confidences = results[0].boxes.conf.cpu().numpy()

                    for i, track_id in enumerate(track_ids):
                        x1, y1, x2, y2 = map(int, boxes[i])
                        confidence = float(confidences[i])

                        # Update track manager
                        self.track_manager.update_track(track_id, frame_num)

                        # Smooth bbox
                        if self.config.ENABLE_SMOOTHING:
                            prev = bbox_cache.get(track_id)
                            smoothed = smooth_bbox(prev, (x1, y1, x2, y2), self.config.BBOX_SMOOTHING_ALPHA)
                            x1, y1, x2, y2 = smoothed
                            bbox_cache[track_id] = smoothed

                        cx = int((x1 + x2) / 2)
                        cy = int((y1 + y2) / 2)

                        in_entrance = point_in_zone(cx, cy, entrance)
                        in_store = point_in_zone(cx, cy, store)
                        in_exit = point_in_zone(cx, cy, exit_zone)

                        # State logic
                        state = person_states.get(track_id, "")
                        if state == "":
                            if in_entrance:
                                person_states[track_id] = "entrance"
                                passby_ids.add(track_id)
                            elif in_store:
                                person_states[track_id] = "rejected"
                        elif state == "entrance" and in_store:
                            person_states[track_id] = "in_store"
                            footfall_ids.add(track_id)
                            if track_id in passby_ids:
                                passby_ids.remove(track_id)

                        is_visitor = (track_id in footfall_ids) and (in_store or in_exit)

                        # Record detection for ground truth analysis
                        classification = self.track_manager.get_classification(track_id, frame_num)
                        self.gt_analyzer.add_detection(
                            frame_num, track_id, (x1, y1, x2, y2), confidence,
                            is_visitor, classification
                        )

                        if is_visitor:
                            # Get existing classification (persistent!)
                            is_locked = classification is not None and track_id in self.track_manager.tracks and \
                                       self.track_manager.tracks[track_id].get('classification_locked', False)

                            # If not locked, try to classify
                            if not is_locked and self.classifier:
                                if frame_num % self.config.CLASSIFY_EVERY_N_FRAMES == 0:
                                    if self.track_manager.should_classify(track_id, frame_num):
                                        crop = frame[y1:y2, x1:x2]
                                        if crop.size > 0:
                                            attrs = self.classifier.classify(crop)
                                            if attrs:
                                                # Add to voting buffer
                                                self.track_manager.add_classification_vote(track_id, attrs)

                                                # Check if we have enough votes to lock
                                                voted = self.track_manager.get_voted_classification(track_id)
                                                if voted:
                                                    # Check overall confidence
                                                    avg_conf = (voted['gender']['confidence'] +
                                                               voted['age']['confidence'] +
                                                               voted['ethnicity']['confidence']) / 3

                                                    if avg_conf >= self.config.CLASSIFICATION_CONFIDENCE_THRESHOLD:
                                                        # LOCK IT
                                                        self.track_manager.set_classification(track_id, voted, frame_num)
                                                        classification = voted
                                                        is_locked = True

                                                        # Count unique visitor
                                                        if track_id not in unique_visitors:
                                                            unique_visitors.add(track_id)
                                                            stats['gender'][voted['gender']['class']] += 1
                                                            stats['age'][voted['age']['class']] += 1
                                                            stats['ethnicity'][voted['ethnicity']['class']] += 1

                                                            if voted['gender']['class'].lower() == 'male':
                                                                male_count += 1
                                                            elif voted['gender']['class'].lower() == 'female':
                                                                female_count += 1

                            # Draw with classification
                            if classification:
                                self._draw_label(frame, (x1, y1, x2, y2), classification, track_id, is_locked)
                            else:
                                cv2.rectangle(frame, (x1, y1), (x2, y2), self.config.COLORS['unknown'], 2)
                                cv2.putText(frame, f"ID: {track_id}", (x1, y1-10),
                                          cv2.FONT_HERSHEY_SIMPLEX, 0.5, (255,255,255), 2)

                        elif in_entrance or (track_id in passby_ids and track_id not in footfall_ids):
                            cv2.rectangle(frame, (x1, y1), (x2, y2), self.config.COLORS['passby_box'], 2)
                            cv2.putText(frame, f"ID: {track_id}", (x1, y1-10),
                                      cv2.FONT_HERSHEY_SIMPLEX, 0.5, self.config.COLORS['passby_box'], 2)

                # Draw UI
                track_stats = self.track_manager.get_stats()
                self._draw_ui(frame, len(footfall_ids), len(passby_ids),
                             len(unique_visitors), male_count, female_count, track_stats)

                writer.write(frame)

                if not self.headless:
                    cv2.imshow("Analytics", cv2.resize(frame, (1280, 720)))
                    if cv2.waitKey(1) & 0xFF == ord('q'):
                        break

                frame_num += 1
                pbar.update(1)

            except Exception as e:
                self.logger.error(f"Frame {frame_num}: {e}")
                frame_num += 1
                pbar.update(1)
                continue

        cap.release()
        writer.release()
        pbar.close()
        if not self.headless:
            cv2.destroyAllWindows()

        duration = (datetime.now() - start_time).total_seconds()

        # ===== EXPORT GROUND TRUTH DATA =====
        self.logger.info("\n📊 Generating comprehensive analytics...")

        # Export CSV
        detections_df = self.gt_analyzer.export_to_csv(out_csv)
        self.logger.info(f"✓ Detections exported: {out_csv}")
        self.logger.info(f"  Total detections: {len(detections_df)}")

        # Generate comprehensive analytics
        analytics_metrics = self.gt_analyzer.generate_comprehensive_analytics("output", video_name + f"_{timestamp}")
        if analytics_metrics:
            self.logger.info(f"\n✓ Analytics generated:")
            self.logger.info(f"  Detection confidence: mean={analytics_metrics['detection_stats']['mean_confidence']:.3f}, "
                           f"median={analytics_metrics['detection_stats']['median_confidence']:.3f}")
            self.logger.info(f"  High confidence detections (≥0.7): {analytics_metrics['detection_stats']['high_confidence_detections']}")
            self.logger.info(f"  Average track length: {analytics_metrics['track_stats']['mean_track_length']:.1f} frames")
            self.logger.info(f"  Files created:")
            for key, path in analytics_metrics.items():
                if key.endswith('_plot'):
                    self.logger.info(f"    - {path}")

        # Report
        total = male_count + female_count
        classifier_stats = self.classifier.get_stats() if self.classifier else {}

        report = {
            'video': video_name,
            'tracker': 'BoT-SORT + ReID (Optimized)',
            'processing_time': duration,
            'fps': round(frame_num / duration, 1) if duration > 0 else 0,
            'footfall': len(footfall_ids),
            'passby': len(passby_ids),
            'unique': len(unique_visitors),
            'male': male_count,
            'male_pct': round(male_count / total * 100, 1) if total > 0 else 0,
            'female': female_count,
            'female_pct': round(female_count / total * 100, 1) if total > 0 else 0,
            'total_classified': total,
            'demographics': dict(stats),
            'tracking': track_stats,
            'classifier': classifier_stats,
            'tracking_errors': tracking_errors,
            'analytics_metrics': analytics_metrics if analytics_metrics else {},
            'config': {
                'confidence': self.config.CONFIDENCE,
                'iou': self.config.IOU_THRESHOLD,
                'ethnicity_fallback': self.config.ETHNICITY_UNKNOWN_FALLBACK,
                'label_smoothing': self.config.ENABLE_LABEL_SMOOTHING
            },
            'output_files': {
                'video': out_video,
                'detections_csv': out_csv,
                'analytics_plots': [v for k, v in analytics_metrics.items() if k.endswith('_plot')] if analytics_metrics else [],
                'report_json': out_json
            }
        }

